<template>
 <div>
     <div class="search">
         <input v-model="keyword" type="text" class="search-input" placeholder="输入城市或拼音">
     </div>
     <div class="search-content" ref="search" v-show="keyword">
         <ul>
             <li class="search-item border-bottom"
                 v-for="item in list"
                 :key="item.id"
                 @click="handleCityClick(item.name)"
             >{{item.name}}</li>
             <li clss="search-item border-bottom" v-show="hasNoData">
                 没有找到匹配的城市
             </li>
         </ul>
     </div>
 </div>
</template>

<script>
import {mapMutations} from 'vuex'
import BScroll from 'better-scroll'
 export default {
   props: {
       cities: Object
   },
   data () {
     return {
         keyword: '', //用于input框中的双向数据绑定
         list:[],
         timer: null

     }
   },
   computed: {
       hasNoData() {
           return !this.list.length
       }
   },
   methods: {
     handleCityClick (city) {
      this.changeCity(city)
      this.$router.push('/')
    },
    ...mapMutations(['changeCity'])
   },
   watch: {
       keyword() {
           if(this.timer) {
               clearTimeout(this.timer)
           }
           if(!this.keyword) {//删掉a是列表内容项就没有了 
               this.list = [];
               return 
           }
           this.timer = setTimeout(() => {//完成城市与输入框之间的匹配功能
               const result = [];
               for(let i in this.cities) {
                   this.cities[i].forEach((value) => {
                       if(value.spell.indexOf(this.keyword) > -1 || value.name.indexOf(this.keyword) > -1) {
                           result.push(value)
                       }  
                   })
               }
               this.list = result
           }, 100)
       }
   },
   mounted() {
       this.scroll = new BScroll(this.$refs.search)
   }
 }
</script>

